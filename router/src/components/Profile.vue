<template>
  <div class="Profile">
    <h1>This is Profile component where user id is ( {{userId }} )</h1>
  </div>
</template>

<script>
export default {
  name: 'Profile',
  data () {
    return {
      userId: this.$route.params.user_id
    }
  },
  methods:{
    updateId(){
      this.userId = this.$route.params.user_id
    }
  },
  watch:{
    $route: 'updateId'
  }
}
</script>

